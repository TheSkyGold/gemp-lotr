package com.gempukku.lotro.cards.set11.rohan;

import com.gempukku.lotro.cards.AbstractCompanion;
import com.gempukku.lotro.cards.TriggerConditions;
import com.gempukku.lotro.cards.effects.AddUntilStartOfPhaseModifierEffect;
import com.gempukku.lotro.common.Culture;
import com.gempukku.lotro.common.Phase;
import com.gempukku.lotro.common.Race;
import com.gempukku.lotro.filters.Filters;
import com.gempukku.lotro.game.PhysicalCard;
import com.gempukku.lotro.game.state.LotroGame;
import com.gempukku.lotro.logic.actions.RequiredTriggerAction;
import com.gempukku.lotro.logic.modifiers.StrengthModifier;
import com.gempukku.lotro.logic.timing.EffectResult;

import java.util.Collections;
import java.util.List;

/**
 * Set: Shadows
 * Side: Free
 * Culture: Rohan
 * Twilight Cost: 2
 * Type: Companion â€¢ Man
 * Strength: 6
 * Vitality: 3
 * Resistance: 7
 * Game Text: Each time Eowyn wins a skirmish, each other [ROHAN] Man is strength +1 until the regroup phase.
 */
public class Card11_146 extends AbstractCompanion {
    public Card11_146() {
        super(2, 6, 3, 7, Culture.ROHAN, Race.MAN, null, "Eowyn", "Shieldmaiden of Rohan", true);
    }

    @Override
    public List<RequiredTriggerAction> getRequiredAfterTriggers(LotroGame game, EffectResult effectResult, PhysicalCard self) {
        if (TriggerConditions.winsSkirmish(game, effectResult, self)) {
            RequiredTriggerAction action = new RequiredTriggerAction(self);
            action.appendEffect(
                    new AddUntilStartOfPhaseModifierEffect(
                            new StrengthModifier(self, Filters.and(Filters.not(self), Culture.ROHAN, Race.MAN), 1), Phase.REGROUP));
            return Collections.singletonList(action);
        }
        return null;
    }
}
